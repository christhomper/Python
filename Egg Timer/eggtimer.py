import time
print('Welcome to the egg timer')
time.sleep(5) # Give time to read greeting
print('I will notify you when it is time to put the egg in the boiling water')
time.sleep(420) # 420 seconds or 7 minutes to bring water to boil
print('Put the egg in the boiling water now')
time.sleep(10) # Gives time to put egg in water before starting cooking time
print('I will notify you 30 seconds before your egg is done..')
time.sleep(420) # 420 seconds or 7 minutes for egg to cook
print('Your egg is nearly cooked')
